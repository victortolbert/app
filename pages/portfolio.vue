          <h2>Diversity Plan</h2>

          <p>
            <b>What is a diversity plan?</b> A diversity plan details how an
            educator will reach their students-regardless of race, ethnicity,
            gender, socioeconomic status, sexuality, disability status,
            nationality, skill level or language ability in a particular
            subject.
          </p>

          <p>
            I plan to teach a Middle School Mathematics class in a way that
            acknowledges diversity and use it as an asset to create a meaningful
            learning environment.
          </p>

          <p>
            To begin a plan we have to understand what our beliefs are about
            diversity. According to Abbe Herzig’s article in Mathematics
            Teacher,
            <em
              >“as we work toward educating diverse students equitably in the
              mathematics classroom, our beliefs and goals for diversity have an
              important impact on our efforts”</em
            >
            (Herzig, 2005). I believe that every student, regardless of
            background, economic status, or skill level, should have an equal
            opportunity to achieve a high-quality education.
          </p>

          <p>
            The first weeks of the school year, observations of students’
            learning styles and habits and will occur.
            <em
              >“One students’ learning preferences may differ from another’s
              based on broad issues such as developmental level, gender, race,
              ethnicity, socioeconomic class, or native language”</em
            >
            (Brown &amp; Knowles, 2007, p. p. 171). Keeping track of
            observations will allow me to understand the individualized learning
            style of each student. Then I will be able to create classroom
            conditions to meet the needs of the different learning styles and
            provide the tools for success at new cognitive challenges (Brown
            &amp; Knowles, 2007, p. 107). After observing students learning
            styles and determining if they are either visual, aural, verbal, or
            kinesthetic, planning lessons that tailor to each method is
            essential. Providing opportunities to use manipulatives, make songs
            about math formulas, solve problems on a whiteboard, or to use
            current technology, is necessary when catering to the diverse
            learning styles. Also providing projects and problems in the
            classroom that connect to the real world or the community in which
            the students live will promote engagement in the classroom (Sleeter,
            2005) (Herzig, 2005). Flex learning time in the classroom will
            appeal to students that may need more time to complete a lesson;
            Collaborative learning with other students in the classroom will
            allow them to learn from each and expose them to people from other
            backgrounds. Students can use technology to research mathematicians
            from different ethnicity, to expose them to people they never knew
            about, and to identify with mathematicians that resemble themselves.
            I also need to
            <em
              >“use assessments that give students authentic opportunities to
              demonstrate their understanding, not confounded by other skills or
              distractors”</em
            >
            (Shepard, 2005) (Herzig, 2005). Also, actively searching for
            diversity trainings within the county and other outside learning
            will keep me current on new methods and philosophies on inclusion.
          </p>

          <p>
            For students with learning, mental, and physical disabilities, I
            will effectively collaborate with co-teacher to make sure I am
            providing for the needs of these students. Modify, but challenging
            work that fits the cognitive level of students with learning
            disabilities will be provided to ensure active participation in each
            lesson. There should never be a time in the class when students with
            disabilities to feel ostracized. I want active participation from
            all students on any level. Reading IEPs and 504 plans to make sure
            accommodations for special needs students are being met are all a
            part of the diversity plan. Understanding the IEPs and 504 plans are
            crucial to providing for special needs students and creating a
            productive learning environment for all students.
          </p>

